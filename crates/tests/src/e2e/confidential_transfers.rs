// End-to-end confidential-transfer check (token-2022 v11.0.0 + zk-sdk 6.0.1).
//
// Exercises the full token-2022 ConfidentialTransfer extension flow against
// a target network and asserts that:
//   1. The auditor can decrypt the on-chain transfer ciphertext to the
//      cleartext amount (validates the ZK ElGamal Proof program at runtime).
//   2. Public balances are consistent post-deposit / post-withdraw.
//
// Migration note (2026-05-13): this test was rewritten off `spl-token-client`
// because that crate hard-pins `spl-token-2022 = ^10.0.0`. All orchestration
// (mint/account create, configure, deposit, transfer, withdraw, context-state
// accounts, record-account chunked writes) is hand-rolled in `token_ops.rs`
// using `spl-token-2022 = 11.0.0` builders directly. Proofs are generated by
// upstream `spl-token-confidential-transfer-proof-generation = 0.6.0` (which
// is internally pinned to `solana-zk-sdk = 6.0.1`).
//
// Localnet: skipped — litesvm bundles a `spl-token-2022` build *without* the
// `zk-ops` feature, so confidential ops return `InvalidInstructionData`
// regardless of proof contents. Run against devnet/testnet/mainnet.

