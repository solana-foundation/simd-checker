// ---------------------------------------------------------------------------
// Confidential transfer flow (split proofs via context-state + record accounts)
// ---------------------------------------------------------------------------

#[allow(clippy::too_many_arguments)]
async fn run_confidential_transfer(
    rpc: &NonblockingRpcClient,
    payer: &Keypair,
    source_owner: &Keypair,
    source_token_account: &Pubkey,
    destination_token_account: &Pubkey,
    _mint: &Pubkey,
    source_elgamal: &ElGamalKeypair,
    source_aes: &AeKey,
    destination_elgamal_pubkey: &ElGamalPubkey,
    auditor_elgamal_pubkey: &ElGamalPubkey,
    transfer_amount: u64,
    sigs: &mut Vec<LabeledTransactionSignature>,
) -> Result<Signature> {
    use spl_token_2022_interface::extension::confidential_transfer::instruction as ct_ix;

    // 1. Fetch source account state.
    info!("confidential_transfers/transfer: fetching source account state...");
    let state = token_ops::get_token_account_state(rpc, source_token_account)
        .await
        .context("fetch source account info")?;
    let ct_acc: &ConfidentialTransferAccount = token_ops::confidential_transfer_extension(&state)?;

    // Decode current available ciphertext + AE-decrypt current decryptable balance.
    let current_available_ct: ElGamalCiphertext = ct_acc
        .available_balance
        .try_into()
        .map_err(|e| anyhow::anyhow!("decode current available balance: {e:?}"))?;
    let current_decryptable_ae: AeCiphertext = ct_acc
        .decryptable_available_balance
        .try_into()
        .map_err(|e| anyhow::anyhow!("decode current decryptable balance: {e:?}"))?;
    let current_balance = current_decryptable_ae
        .decrypt(source_aes)
        .ok_or_else(|| anyhow::anyhow!("decrypt current decryptable available balance"))?;

    // 2. Generate the three transfer proofs (upstream v6).
    info!("confidential_transfers/transfer: generating proofs (equality + ciphertext-validity + range U128)...");
    let proofs = transfer_split_proof_data(
        &current_available_ct,
        &current_decryptable_ae,
        transfer_amount,
        source_elgamal,
        source_aes,
        destination_elgamal_pubkey,
        Some(auditor_elgamal_pubkey),
    )
    .map_err(|e| anyhow::anyhow!("transfer_split_proof_data: {e:?}"))?;
    info!("confidential_transfers/transfer: proofs generated");

    // 3. Create context-state accounts for equality + validity proofs
    //    (each small enough to fit in one tx).
    let proof_authority = Keypair::new();
    let equality_kp = Keypair::new();
    let validity_kp = Keypair::new();
    let range_kp = Keypair::new();

    let sig = token_ops::create_context_state_account(
        rpc,
        payer,
        &equality_kp,
        &proof_authority.pubkey(),
        &proofs.equality_proof_data,
        ProofInstruction::VerifyCiphertextCommitmentEquality,
    )
    .await
    .context("create equality context-state account")?;
    sigs.push(label_sig("transfer-ctx-equality-create", sig));
    info!(
        "confidential_transfers/transfer: equality ctx-state created ({})",
        equality_kp.pubkey()
    );

    let sig = token_ops::create_context_state_account(
        rpc,
        payer,
        &validity_kp,
        &proof_authority.pubkey(),
        &proofs
            .ciphertext_validity_proof_data_with_ciphertext
            .proof_data,
        ProofInstruction::VerifyBatchedGroupedCiphertext3HandlesValidity,
    )
    .await
    .context("create ciphertext-validity context-state account")?;
    sigs.push(label_sig("transfer-ctx-validity-create", sig));
    info!(
        "confidential_transfers/transfer: validity ctx-state created ({})",
        validity_kp.pubkey()
    );

    // 4. Range proof (U128 ≈ 1.4 KB) — write to spl_record account, then
    //    verify_proof_from_account into a context-state account.
    let range_record_kp = Keypair::new();
    info!(
        "confidential_transfers/transfer: writing range U128 proof to record account {} (chunked)...",
        range_record_kp.pubkey()
    );
    let range_proof_bytes = bytemuck::bytes_of(&proofs.range_proof_data);
    let record_results = token_ops::create_record_account(
        rpc,
        payer,
        &range_record_kp,
        &proof_authority,
        range_proof_bytes,
    )
    .await
    .context("create range record account")?;
    for (label, sig) in &record_results {
        sigs.push(label_sig(&format!("transfer-range-{label}"), *sig));
    }
    info!("confidential_transfers/transfer: range proof written to record account");

    let sig = token_ops::create_context_state_account_from_record::<BatchedRangeProofContext>(
        rpc,
        payer,
        &range_kp,
        &proof_authority.pubkey(),
        &range_record_kp.pubkey(),
        spl_record::state::RecordData::WRITABLE_START_INDEX as u32,
        ProofInstruction::VerifyBatchedRangeProofU128,
    )
    .await
    .context("create range context-state account from record")?;
    sigs.push(label_sig("transfer-ctx-range-create", sig));
    info!(
        "confidential_transfers/transfer: range ctx-state created ({})",
        range_kp.pubkey()
    );

    // 5. Compute new decryptable available balance + submit the Transfer ix.
    let new_available_balance = current_balance
        .checked_sub(transfer_amount)
        .ok_or_else(|| anyhow::anyhow!("transfer_amount exceeds current available balance"))?;
    let new_decryptable = source_aes.encrypt(new_available_balance).into();

    info!("confidential_transfers/transfer: submitting Transfer instruction (3 ctx-state account refs)...");
    use spl_token_confidential_transfer_proof_extraction::instruction::ProofLocation;
    let equality_location = ProofLocation::ContextStateAccount(&equality_kp.pubkey());
    let validity_location = ProofLocation::ContextStateAccount(&validity_kp.pubkey());
    let range_location = ProofLocation::ContextStateAccount(&range_kp.pubkey());

    let transfer_ixs = ct_ix::transfer(
        &spl_token_2022_interface::id(),
        source_token_account,
        _mint,
        destination_token_account,
        &new_decryptable,
        &proofs
            .ciphertext_validity_proof_data_with_ciphertext
            .ciphertext_lo,
        &proofs
            .ciphertext_validity_proof_data_with_ciphertext
            .ciphertext_hi,
        &source_owner.pubkey(),
        &[],
        equality_location,
        validity_location,
        range_location,
    )
    .map_err(|e| anyhow::anyhow!("build transfer ix: {e:?}"))?;
    let transfer_sig = token_ops::send_ixs(rpc, payer, &[source_owner], &transfer_ixs)
        .await
        .context("submit Transfer instruction")?;
    sigs.push(label_sig("confidential-transfer", transfer_sig));
    info!(
        "confidential_transfers/transfer: Transfer tx submitted: {}",
        transfer_sig
    );

    // 6. Close ctx-state and record accounts, reclaiming rent.
    info!(
        "confidential_transfers/transfer: closing 3 ctx-state accounts + range record account..."
    );
    for (label_name, ctx_kp) in [
        ("transfer-ctx-equality-close", &equality_kp),
        ("transfer-ctx-validity-close", &validity_kp),
        ("transfer-ctx-range-close", &range_kp),
    ] {
        let sig = token_ops::close_context_state_account(
            rpc,
            payer,
            &proof_authority,
            &ctx_kp.pubkey(),
            &payer.pubkey(),
        )
        .await
        .with_context(|| format!("close context-state account {}", ctx_kp.pubkey()))?;
        sigs.push(label_sig(label_name, sig));
    }
    let sig = token_ops::close_record_account(
        rpc,
        payer,
        &proof_authority,
        &range_record_kp.pubkey(),
        &payer.pubkey(),
    )
    .await
    .context("close range record account")?;
    sigs.push(label_sig("transfer-range-record-close", sig));
    info!("confidential_transfers/transfer: rent reclaimed");

    Ok(transfer_sig)
}

#[allow(clippy::too_many_arguments)]
async fn run_confidential_withdraw(
    rpc: &NonblockingRpcClient,
    payer: &Keypair,
    owner: &Keypair,
    token_account: &Pubkey,
    mint: &Pubkey,
    elgamal: &ElGamalKeypair,
    aes: &AeKey,
    current_balance: u64,
    withdraw_amount: u64,
    sigs: &mut Vec<LabeledTransactionSignature>,
) -> Result<()> {
    use spl_token_2022_interface::extension::confidential_transfer::instruction as ct_ix;

    info!("confidential_transfers/withdraw: fetching source account state...");
    let state = token_ops::get_token_account_state(rpc, token_account)
        .await
        .context("fetch withdraw source account info")?;
    let ct_acc = token_ops::confidential_transfer_extension(&state)?;
    let current_available_ct: ElGamalCiphertext = ct_acc
        .available_balance
        .try_into()
        .map_err(|e| anyhow::anyhow!("decode current available balance: {e:?}"))?;

    info!("confidential_transfers/withdraw: generating proofs (equality + range U64)...");
    let proofs = withdraw_proof_data(
        &current_available_ct,
        current_balance,
        withdraw_amount,
        elgamal,
    )
    .map_err(|e| anyhow::anyhow!("withdraw_proof_data: {e:?}"))?;
    info!("confidential_transfers/withdraw: proofs generated");

    let proof_authority = Keypair::new();
    let equality_kp = Keypair::new();
    let range_kp = Keypair::new();

    let sig = token_ops::create_context_state_account(
        rpc,
        payer,
        &equality_kp,
        &proof_authority.pubkey(),
        &proofs.equality_proof_data,
        ProofInstruction::VerifyCiphertextCommitmentEquality,
    )
    .await
    .context("create withdraw equality context-state account")?;
    sigs.push(label_sig("withdraw-ctx-equality-create", sig));
    info!(
        "confidential_transfers/withdraw: equality ctx-state created ({})",
        equality_kp.pubkey()
    );

    // Range proof (U64) — same record-account pattern the transfer flow uses
    // for its U128 range proof. `BatchedRangeProofU64Data` is
    // BatchedRangeProofContext (~264 B) + PodRangeProofU64 (672 B) ≈ 936 B
    // inline, which combined with `create_account` + a second signer + tx
    // overhead exceeds the 1232-byte raw VersionedTransaction limit. Routing
    // through `spl_record` + `verify_proof_from_account` keeps every on-the-
    // wire tx well under that cap.
    let range_record_kp = Keypair::new();
    info!(
        "confidential_transfers/withdraw: writing range U64 proof to record account {} (chunked)...",
        range_record_kp.pubkey()
    );
    let range_proof_bytes = bytemuck::bytes_of(&proofs.range_proof_data);
    let record_results = token_ops::create_record_account(
        rpc,
        payer,
        &range_record_kp,
        &proof_authority,
        range_proof_bytes,
    )
    .await
    .context("create withdraw range record account")?;
    for (label, sig) in &record_results {
        sigs.push(label_sig(&format!("withdraw-range-{label}"), *sig));
    }
    info!("confidential_transfers/withdraw: range proof written to record account");

    let sig = token_ops::create_context_state_account_from_record::<BatchedRangeProofContext>(
        rpc,
        payer,
        &range_kp,
        &proof_authority.pubkey(),
        &range_record_kp.pubkey(),
        spl_record::state::RecordData::WRITABLE_START_INDEX as u32,
        ProofInstruction::VerifyBatchedRangeProofU64,
    )
    .await
    .context("create withdraw range context-state account from record")?;
    sigs.push(label_sig("withdraw-ctx-range-create", sig));
    info!(
        "confidential_transfers/withdraw: range ctx-state created ({})",
        range_kp.pubkey()
    );

    let new_balance = current_balance - withdraw_amount;
    let new_decryptable = aes.encrypt(new_balance).into();
    use spl_token_confidential_transfer_proof_extraction::instruction::ProofLocation;
    let equality_location = ProofLocation::ContextStateAccount(&equality_kp.pubkey());
    let range_location = ProofLocation::ContextStateAccount(&range_kp.pubkey());

    let withdraw_ixs = ct_ix::withdraw(
        &spl_token_2022_interface::id(),
        token_account,
        mint,
        withdraw_amount,
        MINT_DECIMALS,
        &new_decryptable,
        &owner.pubkey(),
        &[],
        equality_location,
        range_location,
    )
    .map_err(|e| anyhow::anyhow!("build withdraw ixs: {e:?}"))?;
    let withdraw_sig = token_ops::send_ixs(rpc, payer, &[owner], &withdraw_ixs)
        .await
        .context("submit Withdraw instruction")?;
    sigs.push(label_sig("recipient-withdraw", withdraw_sig));
    info!(
        "confidential_transfers/withdraw: Withdraw tx submitted: {}; closing ctx-state accounts + record account...",
        withdraw_sig
    );

    for (label_name, ctx_kp) in [
        ("withdraw-ctx-equality-close", &equality_kp),
        ("withdraw-ctx-range-close", &range_kp),
    ] {
        let sig = token_ops::close_context_state_account(
            rpc,
            payer,
            &proof_authority,
            &ctx_kp.pubkey(),
            &payer.pubkey(),
        )
        .await
        .with_context(|| format!("close withdraw context-state account {}", ctx_kp.pubkey()))?;
        sigs.push(label_sig(label_name, sig));
    }
    let sig = token_ops::close_record_account(
        rpc,
        payer,
        &proof_authority,
        &range_record_kp.pubkey(),
        &payer.pubkey(),
    )
    .await
    .context("close withdraw range record account")?;
    sigs.push(label_sig("withdraw-range-record-close", sig));
    info!("confidential_transfers/withdraw: rent reclaimed");

    Ok(())
}
